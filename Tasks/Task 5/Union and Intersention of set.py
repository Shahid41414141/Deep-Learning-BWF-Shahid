# Using the union() method
set1 = {1, 2, 3}
set2 = {3, 4, 5}
union_set = set1.union(set2)
print(union_set)  

# Using the | operator
union_set = set1 | set2
print(union_set)  

# Using the intersection() method

set1 = {1, 2, 3}
set2 = {3, 4, 5}
intersection_set = set1.intersection(set2)
print(intersection_set)  

# Using the & operator
intersection_set = set1 & set2
print(intersection_set)  
